u16 gl::color = WHITE;

void gl::set_color(u16 color) {
  gl::color = color;
  screen::set_color(gl::color);
}

void gl::put_pixel(u16 x, u16 y) {
  screen::put_pixel(x, y, gl::color);
}

void gl::fill_rect(u16 x, u16 y, u16 w, u16 h) {
  u16 _x = x;
  u16 _y = y;

  for (_y=y; _y < y+h; _y++) {
    for (_x=x; _x < x+w; _x++) {
      screen::put_pixel(_x, _y, gl::color);
    }
  }
}

void gl::draw_rect(u16 x, u16 y, u16 w, u16 h) {
  u16 _x = x;
  while (_x < x+w) {
    screen::put_pixel(_x, y, gl::color);
    screen::put_pixel(_x, y+h-1, gl::color);
    _x += 1;
  }
  u16 _y = y;
  while (_y < y+h) {
    screen::put_pixel(x, _y, gl::color);
    screen::put_pixel(x+w-1, _y, gl::color);
    _y += 1;
  }
}

void gl::draw_line_hor(u16 x, u16 y, u16 l) {
  u16 _x = x;
  while (_x < x+l) {
    screen::put_pixel(_x, y, gl::color);
    _x += 1;
  }
}

void gl::draw_line_vert(u16 x, u16 y, u16 l) {
  u16 _y = y;
  while (_y < y+l) {
    screen::put_pixel(x, _y, gl::color);
    _y += 1;
  }
}

/* have fun with this one */
void gl::print_str(const char *text, u16 x, u16 y) {
  u8 si = 0;
  while (text[si]) {
    char c = text[si];
    for (u8 i =0; i<5; i++) {
#ifdef VIRTUAL
      u8 line = font[(c*5)+i];
#else
      u8 line = pgm_read_byte(font+(c*5)+i);
#endif
      for (u8 j = 0; j<8; j++) {
        if (line & 1) {
          gl::put_pixel(x+i+(si*6), y+j);
        }
        line >>= 1;
      }
    }
    si += 1;
  }

}

void gl::print_str_b(const char *text, u16 x, u16 y) {
  gl::print_str(text, x, y);
  gl::print_str(text, x+1, y);
}

void gl::draw_3d_rect(u16 x, u16 y, u16 w, u16 h) {
  gl::set_color(BLACK);
  gl::draw_line_hor(x, y+h-1, w);
  gl::draw_line_vert(x+w-1, y, h);

  gl::set_color(WHITE);
  gl::draw_line_hor(x+1, y+1, w-3);
  gl::draw_line_vert(x+1, y+1, h-3);

  gl::set_color(DARK_GREY);
  gl::draw_line_hor(x+1, y+h-2, w-2);
  gl::draw_line_vert(x+w-2, y+1, h-2);
}

void gl::draw_glyph(u16 x, u16 y, u8 *a) {
  for (u8 _x=0; _x<7; _x++) {
    u8 b = a[_x];
    for (u8 _y=0; _y<8; _y++) {
      bool draw = b & (1<<_y);
      if (draw) {
        gl::put_pixel(x+_x, y+_y);
      }
    }
  }
}

